    <strong>
        <u>POR QUANTO TEMPO A MUNDOLOGÍSTICA TRATA OS SEUS DADOS PESSOAIS?</u>
    </strong>
</p>
<p>&nbsp;</p>
<p>
    Os seus dados pessoais serão retidos pelo tempo que for necessário para
    cumprir a finalidade para a qual foram coletados, a menos que um período de
    retenção mais longo seja necessário para cumprir obrigações legais,
    resguardar os direitos da MUNDOLOGÍSTICA ou cumprir acordos
    judiciais/extrajudiciais.
</p>
<p>
    Para determinar o período de retenção de seus dados pessoais, a
    MUNDOLOGÍSTICA considera os seguintes critérios:
</p>
<p>
    · Se há uma obrigação legal, contratual ou outra de reter dados pessoais,
    ou se são necessários para fins de investigação ou litígio;
</p>
<p>
    · Se são necessários para manter registros comerciais e financeiros
    precisos.
