<p>
    <strong>DADOS DE NAVEGAÇÃO (COOKIES)</strong>
</p>
<p>&nbsp;</p>
<p>
    <em>Cookies</em>
    são pequenos arquivos de texto enviados pelo site ao computador do usuário
    e que nele ficam armazenados, com informações relacionadas à navegação do
    site.
</p>
<p>
    Por meio dos <em>cookies</em>, pequenas quantidades de informação são
    armazenadas pelo navegador do usuário para que nosso servidor possa lê-las
    posteriormente. Podem ser armazenados, por exemplo, dados sobre o
    dispositivo utilizado pelo usuário, bem como seu local e horário de acesso
    ao site.
</p>
<p>
    Os <em>cookies</em> não permitem que qualquer arquivo ou informação sejam
    extraídos do disco rígido do usuário, não sendo possível, ainda, que, por
    meio deles, se tenha acesso a informações pessoais que não tenham partido
    do usuário ou da forma como utiliza os recursos do site.
</p>
<p>
    É importante ressaltar que nem todo <em>cookie</em> contém informações que
    permitem a identificação do usuário, sendo que determinados tipos de
    cookies podem ser empregados simplesmente para que o site seja carregado
    corretamente ou para que suas funcionalidades funcionem do modo esperado.
</p>
<p>
    As informações eventualmente armazenadas em <em>cookies</em> que permitam
    identificar um usuário são consideradas dados pessoais. Dessa forma, todas
    as regras previstas nesta Política de Privacidade também lhes são
    aplicáveis.
</p>
