    <strong>
        <u>O QUE VOCÊ PRECISA SABER PARA COMPREENDER MELHOR O AVISO?</u>
    </strong>
</p>
<p>&nbsp;</p>
<p>
    <strong><u>Anonimização</u></strong>
    o processo irreversível que remove a possibilidade de um dado identificar o
    Titular ao qual ele originalmente se referia, considerando a utilização de
    meios técnicos razoáveis e disponíveis na ocasião do Tratamento.
</p>
<p>
    <strong><u>Encarregado</u></strong>
    é a pessoa do nosso time responsável por cuidar da sua privacidade aqui na
    MUNDOLOGÍSTICA. Se você tiver qualquer dúvida sobre como os seus dados são
    tratados por nós, é ele quem vai te ajudar.
</p>
<p>
    <strong><u>Dados pessoais</u></strong>
    são todas as informações que permitem a sua identificação de forma direta
    ou que tenham o potencial de o tornar identificável. Como por exemplo, seu
    nome, CPF, número de matrícula, dados da sua conta bancária, perfil
    profissional etc.
</p>
<p>
    <strong><u>Dados Pessoais Sensíveis:</u></strong>
    são quaisquer Dados Pessoais que digam respeito à origem racial ou étnica,
    convicção religiosa, opinião política, filiação a sindicato ou à
    organização de caráter religioso, filosófico ou político, bem como dado
    referente à saúde ou à vida sexual, dado genético ou biométrico.
</p>
<p>
    <strong><u>Tratamento</u></strong>
    <strong><u> de dados pessoais</u></strong>
    significa qualquer operação, como a coleta, utilização, acesso, reprodução,
    compartilhamento, armazenamento, eliminação, realizada com dados pessoais.
</p>
<p>
    Toda vez que houver menção aos termos “MUNDOLOGÍSTICA”, “nós” ou “nossos”,
    estamos nos referindo à MUNDOLOGÍSTICA; de igual modo, toda vez que houver
    menção aos termos “você, “seu” ou “sua”, estamos nos referindo a você,
    Titular.
</p>
